package onepack.twopack.demo.threepack;

public class ThreePackClass {

    private int someValue1;
    private int someValue2;
    public ThreePackClass(int someValue1, int someValue2) {
        this.someValue1 = someValue1;
        this.someValue2 = someValue2;
    }

    public int getSomeValue1() { return someValue1; }
    public int getSomeValue2() { return someValue2; }
}
